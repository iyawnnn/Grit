    <script nonce="{{ app('csp-nonce') }}">
        document.addEventListener('DOMContentLoaded', () => {
            const toggles = document.querySelectorAll('.faq-toggle');

            toggles.forEach(toggle => {
                toggle.addEventListener('click', () => {
                    const answer = toggle.nextElementSibling;
                    const icon = toggle.querySelector('.faq-icon');
                    const arrow = toggle.querySelector('.faq-arrow');
                    const title = toggle.querySelector('.faq-title');

                    const isOpen = answer.classList.contains('grid-rows-[1fr]');

                    // Close all active accordions
                    document.querySelectorAll('.faq-answer').forEach(el => {
                        el.classList.replace('grid-rows-[1fr]', 'grid-rows-[0fr]');
                    });
                    document.querySelectorAll('.faq-icon').forEach(el => {
                        el.classList.remove('bg-[#e26a35]', 'border-[#e26a35]');
                        el.classList.add('bg-gray-50', 'border-gray-200');
                    });
                    document.querySelectorAll('.faq-arrow').forEach(el => {
                        el.classList.remove('rotate-180', 'text-white');
                        el.classList.add('text-gray-500');
                    });
                    document.querySelectorAll('.faq-title').forEach(el => {
                        el.classList.remove('text-[#e26a35]');
                        el.classList.add('text-gray-900');
                    });

                    // Open clicked accordion
                    if (!isOpen) {
                        answer.classList.replace('grid-rows-[0fr]', 'grid-rows-[1fr]');
                        icon.classList.replace('bg-gray-50', 'bg-[#e26a35]');
                        icon.classList.replace('border-gray-200', 'border-[#e26a35]');
                        arrow.classList.replace('text-gray-500', 'text-white');
                        arrow.classList.add('rotate-180');
                        title.classList.replace('text-gray-900', 'text-[#e26a35]');
                    }
                });
            });
        });
    </script>
